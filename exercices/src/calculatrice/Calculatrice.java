package calculatrice;

class Calculatrice {
	
	public float addition (float a, float b) {
		return a+b;
	}
	
	public float soustraction(float a, float b) {
		return a-b;
	}
	
	public float multiplication (float a, float b) {
		return a*b;
	}
	public float division (float a, float b) {
		return a/b;
	}

}
